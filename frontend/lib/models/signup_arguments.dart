class SignupArguments {
  final String firstName;
  final String lastName;
  final String middleName;
  final String? suffix;
  final String username;
  final String password;
  final String email;
  final String phoneNo;
  final String? userType;

  SignupArguments(this.firstName, this.lastName, this.middleName, this.suffix, this.username, this.password, this.email, this.phoneNo, this.userType);
}